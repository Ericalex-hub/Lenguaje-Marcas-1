let cam = document.getElementById("capa");
        function violeta() {
            cam.style.backgroundColor = "violet";
        }
        function verde() {
            cam.style.backgroundColor = "green";
        }
        function rojo() {
            cam.style.backgroundColor = "red";
        }
        function desaparecer() {
            cam.style.visibility = "hidden";
        }
        function aparecer() {
            cam.style.visibility = "visible";
        }
        function normal() {
            cam.style.backgroundColor = "transparent";
            cam.style.visibility = "visible";
        }